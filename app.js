function runCpuStep() {
  if (!state.running || winnerOwner()) return;
  ensurePhaseQueues();
  if (state.phase === "attack") runAttackPhaseStep();
  else runMovePhaseStep();
  render();
  scheduleCpuStep();
}
function ensurePhaseQueues() {
  if (state.phase === "attack" && !state.attackQueue.length && !state.moveQueue.length && !state.passedTownIds.length) {
    state.attackQueue = attackQueueForOwner(state.activeOwner);
  }
}
function runAttackPhaseStep() {
  if (state.phaseActions >= phaseActionLimit) {
    startMovePhase();
    return;
  }
  const attacker = nextOwnedQueuedTown(state.attackQueue);
  if (!attacker) {
    startMovePhase();
    return;
  }
  state.phaseActions += 1;
  if (townDice(attacker) <= 1) {
    passTown(attacker, "pass");
    return;
  }
  const options = attackOptionsForTown(attacker);
  if (!options.length || shouldPassAttack(attacker, options[0])) {
    passTown(attacker, "pass");
    return;
  }
  battle(attacker, options[0].defender);
}
function startMovePhase() {
  state.phase = "move";
  state.phaseActions = 0;
  state.moveQueue = moveQueueForOwner(state.activeOwner, state.passedTownIds);
  if (!state.moveQueue.length) endCpuTurn();
}
function runMovePhaseStep() {
  if (state.phaseActions >= phaseActionLimit) {
    endCpuTurn();
    return;
  }
  const town = nextOwnedQueuedTown(state.moveQueue);
  if (!town) {
    endCpuTurn();
    return;
  }
  state.phaseActions += 1;
  moveDiceFromTown(town);
}
function passTown(town, reason) {
  state.passedTownIds.push(town.id);
  state.selectedId = town.id;
}
function endCpuTurn() {
  recoverOwnerDice(state.activeOwner);
  const previous = state.activeOwner;
  const ownersBeforeSwitch = livingOwners();
  state.activeOwner = nextOwner(state.activeOwner);
  state.phase = "attack";
  state.phaseActions = 0;
  state.attackQueue = attackQueueForOwner(state.activeOwner);
  state.moveQueue = [];
  state.passedTownIds = [];
  if (isOwnerCycleComplete(previous, state.activeOwner, ownersBeforeSwitch)) state.turn += 1;
  playSound("ownerSwitch");
  playSound("attackPhase");
}
function isOwnerCycleComplete(previousOwner, nextActiveOwner, owners) {
  if (owners.length <= 1) return false;
  const previousIndex = owners.indexOf(previousOwner);
  const nextIndex = owners.indexOf(nextActiveOwner);
  return previousIndex >= 0 && nextIndex >= 0 && nextIndex <= previousIndex;
}
function recoverOwnerDice(owner) {
  towns.filter((town) => town.owner === owner).forEach((town) => {
    const baseLimit = town.baseDice || townDiceLimit(town);
    if (townDice(town) < baseLimit) town.currentDice = Math.min(baseLimit, maxTownDice, townDice(town) + 1);
  });
}
function moveDiceFromTown(from) {
  const move = diceMoveOptions(from)[0];
  if (!move || townDice(move.from) <= 1 || townDice(move.to) >= maxMovedTownDice) return false;
  const amount = Math.min(townDice(move.from) - 1, maxMovedTownDice - townDice(move.to));
  if (amount <= 0) return false;
  move.from.currentDice -= amount;
  move.to.currentDice += amount;
  state.selectedId = move.to.id;
  state.lastBattle = `${ownerLabel(from.owner)}\u304c${move.from.name}\u304b\u3089${move.to.name}\u3078\u5175\u529b${amount}\u3092\u79fb\u52d5\u3002`;
  state.logs.push(state.lastBattle);
  return true;
}
function diceMoveOptions(from) {
  const owner = from.owner;
  const owned = towns.filter((town) => town.owner === owner);
  const frontier = new Set(owned.filter((town) => neighborTowns(town).some((other) => other.owner !== owner)).map((town) => town.id));
  const options = [];
  if (townDice(from) <= 1) return options;
  neighborTowns(from).filter((to) => to.owner === owner && townDice(to) < maxMovedTownDice).forEach((to) => {
    const towardFront = frontier.has(to.id) ? 12 : 0;
    const fromBackline = frontier.has(from.id) ? -5 : 3;
    const need = maxMovedTownDice - townDice(to);
    const defendCapital = ownCapitalDefenseScore(to, owner) - ownCapitalDefenseScore(from, owner) * 0.8;
    const pressureCapital = enemyCapitalPathScore(to, owner) - enemyCapitalPathScore(from, owner) * 0.45;
    options.push({ from, to, score: towardFront + fromBackline + need * 2 + defendCapital + pressureCapital + Math.random() });
  });
  return options.sort((a, b) => b.score - a.score);
}
