function graphDistance(from, to, maxDepth = 5) {
  if (!from || !to) return Infinity;
  if (from === to) return 0;
  const cacheKey = `${from.id}|${to.id}|${maxDepth}`;
  if (graphDistanceCache.has(cacheKey)) return graphDistanceCache.get(cacheKey);
  const visited = new Set([from.id]);
  let frontier = [from];
  for (let depth = 1; depth <= maxDepth; depth += 1) {
    const next = [];
    for (const town of frontier) {
      for (const neighbor of neighborTowns(town)) {
        if (visited.has(neighbor.id)) continue;
        if (neighbor === to) {
          graphDistanceCache.set(cacheKey, depth);
          graphDistanceCache.set(`${to.id}|${from.id}|${maxDepth}`, depth);
          return depth;
        }
        visited.add(neighbor.id);
        next.push(neighbor);
      }
    }
    frontier = next;
  }
  graphDistanceCache.set(cacheKey, Infinity);
  return Infinity;
}
function capitalPressureScore(town, attackerOwner) {
  if (town.owner === attackerOwner) return 0;
  const capital = capitalTown(town.owner);
  if (!capital || capital.owner !== town.owner) return 0;
  const distanceToCapital = graphDistance(town, capital, 4);
  if (distanceToCapital === 0) return 95;
  if (distanceToCapital === 1) return 34;
  if (distanceToCapital === 2) return 16;
  return 0;
}
function ownCapitalDefenseScore(town, owner) {
  const capital = capitalTown(owner);
  if (!capital || capital.owner !== owner) return 0;
  const distanceToCapital = graphDistance(town, capital, 4);
  if (!Number.isFinite(distanceToCapital)) return 0;
  const threatLevel = capitalThreatLevel(owner);
  const base = distanceToCapital === 0 ? 38 : distanceToCapital === 1 ? 24 : distanceToCapital === 2 ? 10 : 0;
  const alert = threatLevel === 2 ? 22 : threatLevel === 1 ? 10 : 0;
  return base + alert;
}
function capitalThreatLevel(owner) {
  const capital = capitalTown(owner);
  if (!capital || capital.owner !== owner) return 0;
  const neighbors = neighborTowns(capital);
  if (neighbors.some((neighbor) => neighbor.owner !== owner)) return 2;
  return neighbors.some((neighbor) => neighborTowns(neighbor).some((next) => next.owner !== owner)) ? 1 : 0;
}
